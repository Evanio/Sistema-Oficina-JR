    @Override
    public int retornaIdSelecionado() {
       int linha = tblBusca.getSelectedRow();
       int id = Integer.parseInt(tblBusca.getModel().getValueAt(linha,0).toString());
       return id;
    }

    @Override
    public void preencheFiltro() {
        this.filtro = new Venda();

        if(!fctData.getText().isEmpty())
            filtro.setData(Date.valueOf(fctData.getText()));

        this.filtro.setAliado((Cliente)cbxCliente.getSelectedItem());
        //this.filtro.setStatus((StatusTransacao) cbxFornecedor.getSelectedItem());
    }

    @Override
    public void preencheTabela(List<Venda> listagem) {
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("ID");
        modelo.addColumn("Cliente");
        modelo.addColumn("Data");
        modelo.addColumn("Status");    
        modelo.addColumn("Vencimento");
        
        for(Venda c : listagem){
           if(c.getTipo() == 2){
                Vector linha = new Vector();
                linha.add(c.getId());
                linha.add(c.getAliado().getNome());
                SimpleDateFormat formatarDate = new SimpleDateFormat("dd/MM/yyyy");
                linha.add(String.valueOf(formatarDate.format(c.getData())));
                if(c.getValor_pago() >= c.getValor())
                    linha.add("Pago");
                else
                    linha.add("Pendente");
                linha.add(c.getVencimento());

                modelo.addRow(linha);



            }
        }
        tblBusca.setModel(modelo);
    }

    @Override
    public void limpaFiltro() {
         filtro.setData(null);    
        this.filtro.setAliado(null);
    }


